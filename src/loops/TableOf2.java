package loops;

public class TableOf2 {
	
	public static void main(String[] args) {
		
		int number = 2;
		int multipliedvalue = 1;
		
		while(multipliedvalue<=20)
		{
			int value = number *multipliedvalue;
			
			System.out.println(number+" x "+multipliedvalue+" = "+value);
			
			multipliedvalue++;
			
		}
		
		
		
	}
	
//	WAP to calculate the factorial of a number 5= 5*4*3*2*1

}
