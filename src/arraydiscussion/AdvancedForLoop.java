package arraydiscussion;

public class AdvancedForLoop {
	
	
	
	
	public static void main(String[] args) {
		
		
		int [] a = {10, 20, 30, 8, 5};
		
		for(int w:a)
		{
			System.out.println(w);
		}
		
		String s [] = {"Pune", "Delhi", "Mumbai", "Kolkata", "Chennai"};
		
		for(String w:s)
		{
			System.out.println(w);
		}
		
	}

}
