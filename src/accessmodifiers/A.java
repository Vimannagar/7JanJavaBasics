package accessmodifiers;

public  class A {
	
//	This class is used to discuss protected access modifiers
	
	protected int a  = 80;
	
	protected static char u  = '@';
	
	protected void m9()
	{
		System.out.println("Protected non static m9 method from A class");
	}
	
	
	protected static void m10()
	{
		System.out.println("Protected static m10 method from A class");
	}

}
